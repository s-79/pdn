        <p class="textPres text-justify col-12 col-md-9 m-auto">
        En octobre 2017, La Ligue de l'enseignement – FOL 93 est retenue pour porter la coordination du réseau des Promeneurs du Net en Seine-Saint-Denis. Dans le même temps, les structures ainsi que les professionel-les souhaitant s'inscrire dans la démarche sont identifiés.</br></br>
        En novembre 2017, le réseau est officiellement lancé sur le département. Il est constitué au départ de 12 structures jeunesses conventionnées et de 15 professionnel-les labellisé-es « Promeneurs du Net » impactant 12 communes du territoire.</br></br>
        Après une première phase expérimentale d'une année, le dispositif se poursuit en Seine-Saint-Denis, avec son réseau d'acteurs éducatifs qui se déploient d’années en années toujours coordonné et accompagné par la Ligue de l'enseignement – FOL 93.</p>
